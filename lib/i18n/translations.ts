function normalize(value: string) { return value.replace(/\s+/g, " ").trim(); }
function preserve(value: string, translated: string) {
  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

export function translateArabicText(value: string): string {
  if (!/[\u0600-\u06FF]/.test(value)) return value;
  const normalized = normalize(value);
  if (exact[normalized]) return preserve(value, exact[normalized]);
  let result = normalized;
  for (const [ar, en] of phrasePairs) result = result.split(ar).join(en);
  result = result.replace(/[\u0600-\u06FF]+/g, word => words[word] ?? word);
  return preserve(value, result);
}
